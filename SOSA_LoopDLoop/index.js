let km = 0;
let velocidad = 9;


while (km < 10) {
    for (km = 3; km <= 10; km *= 3) {
        if (velocidad < 9) {
            console.log("Aumenta tu velocidad para obtener un caramelo");
        } else {
            console.log("Llegaste al km " + km + " toma un caramelo");
        } 
    }; 

    km++; 
};
   

    





